// TestNewDetectionBaseline_EdgeBlockGate is the regression guard for the
// WAF-block-baseline false positive: a baseline that is a vendor WAF/CDN edge
// block must be flagged so both probe legs (the 403→200 status transition and the
// tag-structure differential) drop the endpoint rather than compare a probe that
// merely evaded the edge rule against a block page. Crucially, a genuine
// application 403 must NOT be flagged, so the real access-control-flip signal is
// preserved.
func TestNewDetectionBaseline_EdgeBlockGate(t *testing.T) {
	// A CloudFront "Request blocked" 403 baseline is an edge block.
	cf := "HTTP/1.1 403 Forbidden\r\nServer: CloudFront\r\nContent-Type: text/html\r\n\r\n" +
		"<html><body><H1>403 ERROR</H1>Request blocked. Generated by cloudfront (CloudFront)</body></html>"
	cfEntry := &modkit.BaselineEntry{Response: httpmsg.NewHttpResponse([]byte(cf)), StatusCode: 403}
	assert.True(t, newDetectionBaseline(cfEntry).edgeBlocked,
		"a CloudFront edge-block baseline must be flagged edgeBlocked")

	// A Cloudflare challenge served AT 200 (Cf-Mitigated) is also an edge block.
	chal := "HTTP/1.1 200 OK\r\nCf-Mitigated: challenge\r\nContent-Type: text/html\r\n\r\n" +
		"<html><body>Just a moment...</body></html>"
	chalEntry := &modkit.BaselineEntry{Response: httpmsg.NewHttpResponse([]byte(chal)), StatusCode: 200}
	assert.True(t, newDetectionBaseline(chalEntry).edgeBlocked,
		"a 200-status Cloudflare challenge baseline must be flagged edgeBlocked")

	// A genuine application 403 (nginx) is NOT an edge block — the real
	// access-control signal must be preserved.
	app := "HTTP/1.1 403 Forbidden\r\nServer: nginx\r\nContent-Type: text/html\r\n\r\n" +
		"<html><body>Forbidden</body></html>"
	appEntry := &modkit.BaselineEntry{Response: httpmsg.NewHttpResponse([]byte(app)), StatusCode: 403}
	assert.False(t, newDetectionBaseline(appEntry).edgeBlocked,
		"a genuine application 403 must NOT be flagged edgeBlocked")
}

func TestTagDistance(t *testing.T) {
	a := extractTagCounts("<html><body><div></div><div></div></body></html>")

	// Reordering the same tags must register zero distance — exact string
	// comparison would have flagged this.
	reordered := extractTagCounts("<div><div></div></div><body></body><html></html>")
	assert.Equal(t, 0, tagDistance(a, reordered), "reordered identical tag set must be distance 0")

	// One added <div> is distance 1.
	added := extractTagCounts("<html><body><div></div><div></div><div></div></body></html>")
	assert.Equal(t, 1, tagDistance(a, added), "one added tag must be distance 1")

	// Empty vs populated counts every tag.
	assert.Equal(t, 4, tagDistance(nil, a))
}

// TestConfirmChange_TagLegHighVariancePageNotConfirmed reproduces the tag-leg
// false positive: a page whose HTML tag COUNT changes on every render (rotating
// feed / personalized SSR shell / A-B block) that the 2-sample jitter calibration
// under-sampled. The probe re-fetch diverges from the baseline (so the old
// single-baseline confirm would accept it), but a fresh no-payload control diverges
// just as much — proving the delta is ambient page variance, not input handling —
// so confirmChange must reject it.
func TestConfirmChange_TagLegHighVariancePageNotConfirmed(t *testing.T) {
	t.Parallel()
	var n int64
	srv := httptest.NewServer(http.HandlerFunc(func(w http.ResponseWriter, _ *http.Request) {
		// Always many tags, but a DIFFERENT count every request → any fetch diverges
		// far from a small fixed baseline, for the probe AND the no-payload control.
		c := atomic.AddInt64(&n, 1)
		_, _ = w.Write([]byte("<html><body>" + strings.Repeat("<div>x</div>", int(20+c%13)) + "</body></html>"))
	}))
	defer srv.Close()

	client := modtest.Requester(t)
	rr := modtest.Request(t, srv.URL+"/feed?q=seed")
	baseline := &detectionBaseline{
		tagCounts:  extractTagCounts("<html><body><div>x</div></body></html>"),
		statusCode: 200,
		tagJitter:  2, // under-sampled: the page actually swings by ~20 tags
	}
	change := &behaviorChange{TagsChanged: true, IsInteresting: true}

	ok := confirmChange(rr, client, baseline, rr.Request().Raw(), change)
	assert.False(t, ok, "a page whose tag count varies every request must not confirm a tag-leg change: the no-payload control diverges too")
}

